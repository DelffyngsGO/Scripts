let pontuação = Number(prompt("Insira sua pontuação: "))
if (pontuação <= 10) {
    alert("Deu ruim ")
} else if (pontuação > 10 , or <= 100) {
    alert("Tá... melhorando hein ")
} else if (pontuação > 100 , or <= 200) {
    alert("SUPIMPA! ")
} else if (pontuação > 200) {
    alert("MITOU!!")
}